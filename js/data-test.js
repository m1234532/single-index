// 随机打乱函数
function shuffleArray(array){var a=[].concat(array);for(var i=a.length-1;i>0;i--){var j=Math.floor(Math.random()*(i+1));var t=a[i];a[i]=a[j];a[j]=t}return a}

// 生成随机16题
function getRandomQuestions(){
  var dims=['social','filter','heartbeat','alone'];
  var result=[];
  dims.forEach(function(dim){
    var dq=questionBank.filter(function(q){return q.dim===dim});
    var shuffled=shuffleArray(dq);
    result=result.concat(shuffled.slice(0,4));
  });
  result=shuffleArray(result);
  result.forEach(function(q){
    var indices=[0,1,2,3];
    var si=shuffleArray(indices);
    q.optMap=si;
    q.options=si.map(function(i){return q.options[i]});
  });
  return result;
}


/**
 * 计算测试结果
 * @param {Array} answers - 用户答案数组（0-3）
 * @param {Array} questions - 题目数组（用于解析 optMap）
 * @returns {Object} 完整结果对象
 */
function calcResult(answers, questions) {
  // 统计各维度得分
  const dimScores = {
    social: { a: 0, b: 0 },
    filter: { a: 0, b: 0 },
    heartbeat: { a: 0, b: 0 },
    alone: { a: 0, b: 0 }
  };
  
  questions.forEach((q, i) => {
    const ans = answers[i];
    const origIdx = q.optMap ? q.optMap[ans] : ans;
    // 选项0,1算a（高分），2,3算b（低分）
    if (origIdx === 0 || origIdx === 1) {
      dimScores[q.dim].a += 1;
    } else {
      dimScores[q.dim].b += 1;
    }
  });
  
  // 生成类型码
  const typeCode = 
    getDimensionLevel('social', dimScores.social.a) +
    getDimensionLevel('filter', dimScores.filter.a) +
    getDimensionLevel('heartbeat', dimScores.heartbeat.a) +
    getDimensionLevel('alone', dimScores.alone.a);
  
  // 获取人格数据
  const persona = PERSONALITY_DATA[typeCode] || { name: '神秘单身者', emoji: '❓' };
  const tips = generateTips(typeCode);
  const visual = getVisualStyle(typeCode);
  
  // 生成维度标签
  const dims = typeCode.split('').map(letter => 
    `${letter}(${letterInfo[letter].label})`
  );
  
  return {
    name: persona.name,
    subtitle: `${typeCode} · ${persona.name}`,
    type: typeCode,
    index: calculateIndex({
      social: dimScores.social.a,
      filter: dimScores.filter.a,
      heartbeat: dimScores.heartbeat.a,
      alone: dimScores.alone.a
    }),
    quote: persona.quote,
    desc: persona.desc,
    tips: tips.tips,
    avoid: tips.avoid,
    tags: dims,
    dims: dims,
    dimScores: dimScores,
    visual: visual
  };
}


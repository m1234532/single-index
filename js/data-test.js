// 单身指数计算：越高越难脱单
function calcIndex(sa, fa, ha, aa) {
  // 社交差 + 标准高 + 心动少 + 独立高 → 单身指数高
  // sa=社交高分(0-4), fa=标准高分, ha=心动高分, aa=独立高分
  const base = 50;
  // 社交好(sa高)容易脱单→降低指数；社交差(sa低)→指数高
  const socialReduce = [0, -6, -12, -18, -25][sa];
  // 标准高(fa高)难脱单→增加指数
  const filterAdd = [0, 6, 13, 21, 30][fa];
  // 心动多(ha高)容易脱单→降低指数
  const heartReduce = [0, -5, -10, -16, -23][ha];
  // 独立强(aa高)难脱单→增加指数
  const aloneAdd = [0, 5, 11, 18, 26][aa];
  const raw = base + socialReduce + heartReduce + filterAdd + aloneAdd;
  return Math.max(2, Math.min(99, raw));
}


function calcIndex(sa,fa,ha,aa){
  var base=50;
  var socialReduce=[0,-6,-12,-18,-25][sa];
  var filterAdd=[0,6,13,21,30][fa];
  var heartReduce=[0,-5,-10,-16,-23][ha];
  var aloneAdd=[0,5,11,18,26][aa];
  return Math.max(2,Math.min(99,base+socialReduce+heartReduce+filterAdd+aloneAdd));
}


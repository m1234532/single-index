/**
 * Fisher-Yates 洗牌算法
 * @param {Array} array - 待打乱数组
 * @returns {Array} 新数组（不修改原数组）
 */
function shuffleArray(array) {
  const arr = [...array];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

/**
 * 生成随机题目（每维度4题，共16题）
 * 选项顺序也会随机打乱
 * @returns {Array} 题目数组
 */
function generateQuestions() {
  const dims = ['social', 'filter', 'heartbeat', 'alone'];
  let result = [];
  
  dims.forEach(dim => {
    const dimQuestions = QUESTION_BANK.filter(q => q.dim === dim);
    const shuffled = shuffleArray(dimQuestions);
    result = result.concat(shuffled.slice(0, 4));
  });
  
  // 打乱题目顺序
  result = shuffleArray(result);
  
  // 打乱每道题的选项顺序，并记录映射关系
  result.forEach(q => {
    const indices = [0, 1, 2, 3];
    const shuffledIndices = shuffleArray(indices);
    q.optMap = shuffledIndices;
    q.options = shuffledIndices.map(i => q.options[i]);
  });
  
  return result;
}

/**
 * 根据得分判断维度档位
 * @param {string} dim - 维度名
 * @param {number} aCount - 高分选项计数
 * @returns {string} 档位字母
 */
function getDimensionLevel(dim, aCount) {
  const thresholds = CONFIG.thresholds[dim];
  const levels = CONFIG.dimensions[dim].levels;
  
  for (let i = thresholds.length - 1; i >= 0; i--) {
    if (aCount >= thresholds[i]) {
      return levels[i];
    }
  }
  return levels[0];
}

/**
 * 计算单身指数
 * @param {Object} scores - 各维度得分 {social, filter, heartbeat, alone}
 * @returns {number} 0-99 的指数
 */
function calculateIndex(scores) {
  const w = CONFIG.indexWeights;
  let index = w.base;
  
  index += w.social[scores.social] || 0;
  index += w.filter[scores.filter] || 0;
  index += w.heart[scores.heartbeat] || 0;
  index += w.alone[scores.alone] || 0;
  
  return Math.max(2, Math.min(99, index));
}


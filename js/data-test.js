function calcResult(answers){
  var dimScores={social:{a:0,b:0},filter:{a:0,b:0},heartbeat:{a:0,b:0},alone:{a:0,b:0}};
  questions.forEach(function(q,i){
    var ans=answers[i];
    var origIdx=q.optMap?q.optMap[ans]:ans;
    if(origIdx===0||origIdx===1) dimScores[q.dim].a+=1;
    else dimScores[q.dim].b+=1;
  });
  var sl=dimScores.social.a>=3?'A':dimScores.social.a>=2?'V':'R';
  var fl=dimScores.filter.a>=3?'P':dimScores.filter.a>=2?'F':'O';
  var hl=dimScores.heartbeat.a>=3?'S':dimScores.heartbeat.a>=2?'T':'C';
  var al=dimScores.alone.a>=3?'I':dimScores.alone.a>=2?'N':'D';
  var gradeKey=sl+fl+hl+al;
  return buildResult81(gradeKey,dimScores);
}


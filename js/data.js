const questions = getRandomQuestions();

function calcResult(answers) {
  const dimScores={social:{a:0,b:0},filter:{a:0,b:0},heartbeat:{a:0,b:0},alone:{a:0,b:0}};
  answers.forEach((ans,i)=>{
    const q = questions[i];
    if(!q) return;
    // 前3个选项偏向外向/高需求，后3个偏向内向/低需求
    if(ans===0||ans===1||ans===2) dimScores[q.dim].a+=1;
    else dimScores[q.dim].b+=1;
  });
  const type=
    (dimScores.social.a>3?'E':'H')+
    (dimScores.filter.a>3?'P':'R')+
    (dimScores.heartbeat.a>3?'S':'T')+
    (dimScores.alone.a>3?'I':'C');
  return {
    ...(results[type]||results.HRTI),
    dimScores
  };
}

